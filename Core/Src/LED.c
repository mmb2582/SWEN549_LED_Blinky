#include "LED.h"

//******************************************************************************************
// Green LED = PB0
//******************************************************************************************
#define LED_PIN 0

//******************************************************************************************
// Turn LED On
//******************************************************************************************
void LED_On(void){
	GPIOB->ODR |= (1UL<<LED_PIN);
}

//******************************************************************************************
// Turn LED Off
//******************************************************************************************
void LED_Off(void){
	GPIOB->ODR &= ~(1UL<<LED_PIN);
}

//******************************************************************************************
// Toggle LED 
//******************************************************************************************
void LED_Toggle(void){
	GPIOB->ODR ^= (1UL<<LED_PIN);
}
